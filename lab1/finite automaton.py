states = ["Выключен", "Нагрев", "Поддержание температуры"]
current_state = states[0]  # Начальное состояние "Выключен"

# Переходы (индексы состояний)
transitions = [
    # Переходы для состояния "Выключен"
    {"Включить": 1},

    # Переходы для состояния "Нагрев"
    {"Температура достигнута": 2, "Охлаждение": 1, "Выключить": 0},

    # Переходы для состояния "Поддержание температуры"
    {"Охлаждение": 1, "Выключить": 0}
]

# Выводы (индикаторы для каждого состояния)
outputs = [
    "Подсветка не горит",  # Выключен
    "Горит оранжевая подсветка",  # Нагрев
    "Горит синяя подсветка"  # Поддержание температуры
]


# Функция для выполнения перехода
def transition(signal, current_state):
    state_index = states.index(current_state)
    if signal in transitions[state_index]:
        return states[transitions[state_index][signal]]
    return current_state  # Если сигнал не найден, состояние не меняется


# Функция для получения текущего вывода
def get_output(current_state):
    return outputs[states.index(current_state)]


# Бесконечный цикл для приёма входных сигналов
while True:
    print(f"Текущее состояние: {current_state}")
    print(f"Индикация(выходной сигнал): {get_output(current_state)}")

    # Получение сигнала от пользователя
    signal = input("Введите сигнал (Включить, Температура достигнута, Охлаждение, Выключить, Выйти): ")

    if signal == "Выйти":
        break
    # Переход в новое состояние
    current_state = transition(signal, current_state)

    print("\n")  # Для визуального разделения шагов
